def geocode(name: str) -> tuple[float, float] | None:
    """Return (lat, lon) for a location name, or None if not found."""
    key = name.lower().strip()
    if key in LOCATIONS:
        lat, lon, _ = LOCATIONS[key]
        return (lat, lon)
    # Try partial match (last word drop for genitive case)
    parts = key.split()
    if len(parts) > 1:
        shorter = " ".join(parts[:-1])
        if shorter in LOCATIONS:
            lat, lon, _ = LOCATIONS[shorter]
            return (lat, lon)
    return None


def get_location_type(name: str) -> str | None:
    key = name.lower().strip()
    if key in LOCATIONS:
        return LOCATIONS[key][2]
    return None


